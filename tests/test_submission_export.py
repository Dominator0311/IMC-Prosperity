"""Tests for the Phase 9 submission export + validation pipeline.

There are three logical layers we want to protect here:

1. **Exporter unit tests** — the bundler deterministically collapses
   the live path into one string, respects the module order, strips
   internal imports, and handles both datamodel modes correctly.

2. **Validator unit tests** — the validator flags clearly-broken
   submissions (forbidden imports, missing Trader, residual src
   imports) and passes clean ones.

3. **End-to-end dry run** — with ``--datamodel=inline`` the bundled
   file is self-contained. We write it to ``tmp_path``, load it with
   ``importlib.util.spec_from_file_location`` (carefully registering
   the module in ``sys.modules`` so dataclass decorators can resolve
   their own class ``__module__``), instantiate ``Trader``, and run
   it on a minimal ``TradingState``. This is the strict
   proof-by-execution that replaces the old 30-line stub.

The dry-run test deliberately avoids any reliance on ``src/``. If you
accidentally break the bundler, these tests fail loudly.
"""

