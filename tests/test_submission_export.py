    import src.datamodel as local_datamodel

    sentinel = sys.modules.get("datamodel")
    sys.modules["datamodel"] = local_datamodel
    module_name = "_phase9_smoke_platform"
    try:
        module = _load_bundle_module(path, module_name)
        trader = module.Trader()
        state = _empty_trading_state(local_datamodel)
        result = trader.run(state)
        assert len(result) == 3
    finally:
        sys.modules.pop(module_name, None)
        if sentinel is None:
            sys.modules.pop("datamodel", None)
        else:
            sys.modules["datamodel"] = sentinel


